
public class program_5_1 {  // i.e class floatpoint

    public static void main(String[] args) {
        float a = 20.5f;
        float b = 6.4f;
        
        System.out.println("a ="+a);
        System.out.println("b ="+b);
        System.out.println("a+b ="+(a+b));
        System.out.println("a-b ="+(a-b));
        System.out.println("a*b ="+(a*b));
        System.out.println("a/b ="+(a/b));
        System.out.println("a%b ="+(a%b));
    }
}
