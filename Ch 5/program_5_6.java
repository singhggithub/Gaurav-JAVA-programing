
public class program_5_6 {
    public static void main(String[] args) {
        double x;
        x = Math.max(22, 33);
        System.out.println("The maximum among 22 , 33 is = " + x);

        x = Math.sqrt(64);
        System.out.println("The square root of 64 is = " + x);

        x = Math.abs(-55);  // convert -ve to +ve
        System.out.println("The absolute value of -55 is = " + x);

        x = Math.pow(16, 3);
        System.out.println("The cube of 16 is =  " + x);

        x = Math.min(22, 33);
        System.out.println("The minimum among 22 and 33 is = " + x);

        x = Math.log(3);
        System.out.println("The natural log of 3 is = " + x);

    }
}
